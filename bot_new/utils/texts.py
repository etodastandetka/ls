def get_casino_name(bookmaker: str) -> str:
    """–ü–æ–ª—É—á–∞–µ—Ç –æ—Ç—Ñ–æ—Ä–º–∞—Ç–∏—Ä–æ–≤–∞–Ω–Ω–æ–µ –Ω–∞–∑–≤–∞–Ω–∏–µ –±—É–∫–º–µ–∫–µ—Ä–∞"""
    if not bookmaker:
        return ''
    bookmaker_lower = bookmaker.lower()
    return CASINO_NAMES.get(bookmaker_lower, bookmaker.upper())

def get_text(key: str, lang: str = 'ru', **kwargs) -> str:
    """–ü–æ–ª—É—á–∞–µ—Ç –ø–µ—Ä–µ–≤–µ–¥–µ–Ω–Ω—ã–π —Ç–µ–∫—Å—Ç —Å –ø–æ–¥—Å—Ç–∞–Ω–æ–≤–∫–æ–π –ø–µ—Ä–µ–º–µ–Ω–Ω—ã—Ö"""
    translations = TRANSLATIONS.get(lang, TRANSLATIONS['ru'])
    text = translations.get(key, key)
    
    # –ï—Å–ª–∏ –µ—Å—Ç—å {casino_name} –≤ —Ç–µ–∫—Å—Ç–µ, –Ω–æ –Ω–µ –ø–µ—Ä–µ–¥–∞–Ω–æ, –ø—ã—Ç–∞–µ–º—Å—è –ø–æ–ª—É—á–∏—Ç—å –∏–∑ kwargs
    if '{casino_name}' in text and 'casino_name' not in kwargs:
        casino_key = kwargs.get('bookmaker') or kwargs.get('casino') or ''
        kwargs['casino_name'] = get_casino_name(casino_key)
    
    # –£–¥–∞–ª—è–µ–º {logo_emoji} –∏–∑ kwargs, —á—Ç–æ–±—ã –Ω–µ –±—ã–ª–æ –æ—à–∏–±–∫–∏ –ø—Ä–∏ —Ñ–æ—Ä–º–∞—Ç–∏—Ä–æ–≤–∞–Ω–∏–∏
    # –õ–æ–≥–æ—Ç–∏–ø –±—É–¥–µ—Ç –æ–±—Ä–∞–±–æ—Ç–∞–Ω –æ—Ç–¥–µ–ª—å–Ω–æ –≤ get_text_with_premium_emoji
    logo_emoji_placeholder = '{logo_emoji}' in text
    if logo_emoji_placeholder:
        # –í—Ä–µ–º–µ–Ω–Ω–æ –∑–∞–º–µ–Ω—è–µ–º –Ω–∞ –ø—É—Å—Ç—É—é —Å—Ç—Ä–æ–∫—É –¥–ª—è —Ñ–æ—Ä–º–∞—Ç–∏—Ä–æ–≤–∞–Ω–∏—è
        text = text.replace('{logo_emoji}', '')
    
    # –ü–æ–¥—Å—Ç–∞–≤–ª—è–µ–º –ø–µ—Ä–µ–º–µ–Ω–Ω—ã–µ
    try:
        formatted_text = text.format(**kwargs)
        # –í–æ–∑–≤—Ä–∞—â–∞–µ–º –ø–ª–µ–π—Å—Ö–æ–ª–¥–µ—Ä –æ–±—Ä–∞—Ç–Ω–æ, –µ—Å–ª–∏ –æ–Ω –±—ã–ª
        if logo_emoji_placeholder:
            formatted_text = formatted_text.replace('', '{logo_emoji}', 1) if '{logo_emoji}' not in formatted_text else formatted_text
            # –ë–æ–ª–µ–µ –Ω–∞–¥–µ–∂–Ω—ã–π —Å–ø–æ—Å–æ–± - –≤—Å—Ç–∞–≤–ª—è–µ–º –≤ –∫–æ–Ω–µ—Ü —Å—Ç—Ä–æ–∫–∏ –ø–µ—Ä–µ–¥ –ø–µ—Ä–≤—ã–º –ø–µ—Ä–µ–Ω–æ—Å–æ–º
            if '{logo_emoji}' not in formatted_text:
                # –ù–∞—Ö–æ–¥–∏–º –ø–æ–∑–∏—Ü–∏—é –ø–æ—Å–ª–µ "LUX ON!"
                luxon_pos = formatted_text.find('LUX ON!')
                if luxon_pos != -1:
                    insert_pos = luxon_pos + len('LUX ON!')
                    formatted_text = formatted_text[:insert_pos] + ' {logo_emoji}' + formatted_text[insert_pos:]
        return formatted_text
    except KeyError as e:
        import logging
        logger = logging.getLogger(__name__)
        logger.warning(f"‚ö†Ô∏è –û—Ç—Å—É—Ç—Å—Ç–≤—É–µ—Ç –ø–µ—Ä–µ–º–µ–Ω–Ω–∞—è {e} –≤ —Ç–µ–∫—Å—Ç–µ '{key}'")
        # –í–æ–∑–≤—Ä–∞—â–∞–µ–º –ø–ª–µ–π—Å—Ö–æ–ª–¥–µ—Ä –æ–±—Ä–∞—Ç–Ω–æ, –µ—Å–ª–∏ –æ–Ω –±—ã–ª
        if logo_emoji_placeholder and '{logo_emoji}' not in text:
            luxon_pos = text.find('LUX ON!')
            if luxon_pos != -1:
                insert_pos = luxon_pos + len('LUX ON!')
                text = text[:insert_pos] + ' {logo_emoji}' + text[insert_pos:]
        return text


def get_text_with_premium_emoji(key: str, lang: str = 'ru', **kwargs) -> tuple[str, list]:
    """–ü–æ–ª—É—á–∞–µ—Ç —Ç–µ–∫—Å—Ç —Å –ø—Ä–µ–º–∏—É–º —ç–º–æ–¥–∑–∏
    
    Returns:
        tuple: (—Ç–µ–∫—Å—Ç, —Å–ø–∏—Å–æ–∫ entities –¥–ª—è –ø—Ä–µ–º–∏—É–º —ç–º–æ–¥–∑–∏)
    """
    from utils.premium_emoji import add_premium_emoji_to_text
    from utils.premium_emoji import _utf16_offset, _utf16_len
    from config import Config
    from aiogram.types import MessageEntity
    from aiogram.enums import MessageEntityType
    
    text = get_text(key, lang, **kwargs)
    
    # –°–ø–µ—Ü–∏–∞–ª—å–Ω–∞—è –æ–±—Ä–∞–±–æ—Ç–∫–∞ –¥–ª—è main_menu_text - –¥–æ–±–∞–≤–ª—è–µ–º –ª–æ–≥–æ—Ç–∏–ø
    if key == 'main_menu_text' and '{logo_emoji}' in text:
        # –£–±–∏—Ä–∞–µ–º –ø–ª–µ–π—Å—Ö–æ–ª–¥–µ—Ä –∏–∑ —Ç–µ–∫—Å—Ç–∞
        text_clean = text.replace(' {logo_emoji}', '').replace('{logo_emoji} ', '').replace('{logo_emoji}', '')
        
        # –ù–∞—Ö–æ–¥–∏–º –ø–æ–∑–∏—Ü–∏—é –ø–æ—Å–ª–µ "LUX ON!" –¥–ª—è –≤—Å—Ç–∞–≤–∫–∏ –ª–æ–≥–æ—Ç–∏–ø–∞
        luxon_pos = text_clean.find('LUX ON!')
        if luxon_pos == -1:
            luxon_pos = text_clean.find('LUX ON')
        
        # –ò—Å–ø–æ–ª—å–∑—É–µ–º –æ–±—ã—á–Ω—ã–π —ç–º–æ–¥–∑–∏ –∫–∞–∫ –∑–∞–≥–ª—É—à–∫—É (–±—É–¥–µ—Ç –∑–∞–º–µ–Ω–µ–Ω –Ω–∞ –ø—Ä–µ–º–∏—É–º –ª–æ–≥–æ—Ç–∏–ø)
        logo_placeholder_emoji = "üî∑"  # –í—Ä–µ–º–µ–Ω–Ω—ã–π —ç–º–æ–¥–∑–∏, –∫–æ—Ç–æ—Ä—ã–π –∑–∞–º–µ–Ω–∏–º –Ω–∞ –ª–æ–≥–æ—Ç–∏–ø
        
        if luxon_pos != -1:
            # –í—Å—Ç–∞–≤–ª—è–µ–º —ç–º–æ–¥–∑–∏-–∑–∞–≥–ª—É—à–∫—É –ø–æ—Å–ª–µ "LUX ON!"
            insert_pos = luxon_pos + len('LUX ON!') if 'LUX ON!' in text_clean else luxon_pos + len('LUX ON')
            text_with_placeholder = text_clean[:insert_pos] + ' ' + logo_placeholder_emoji + text_clean[insert_pos:]
        else:
            # –ï—Å–ª–∏ –Ω–µ –Ω–∞—à–ª–∏, –¥–æ–±–∞–≤–ª—è–µ–º –≤ –∫–æ–Ω–µ—Ü –ø–µ—Ä–≤–æ–π —Å—Ç—Ä–æ–∫–∏
            first_line_end = text_clean.find('\n')
            if first_line_end != -1:
                text_with_placeholder = text_clean[:first_line_end] + ' ' + logo_placeholder_emoji + text_clean[first_line_end:]
                insert_pos = first_line_end
            else:
                text_with_placeholder = text_clean + ' ' + logo_placeholder_emoji
                insert_pos = len(text_clean)
        
        # –ü—Ä–∏–º–µ–Ω—è–µ–º –ø—Ä–µ–º–∏—É–º —ç–º–æ–¥–∑–∏ –∫ —Ç–µ–∫—Å—Ç—É (—ç—Ç–æ —Å–æ–∑–¥–∞—Å—Ç entities –¥–ª—è –æ–±—ã—á–Ω—ã—Ö —ç–º–æ–¥–∑–∏, –Ω–æ –Ω–µ –¥–ª—è –∑–∞–≥–ª—É—à–∫–∏)
        text_with_emoji, entities = add_premium_emoji_to_text(text_with_placeholder, Config.PREMIUM_EMOJI_MAP)
        
        # –ù–∞—Ö–æ–¥–∏–º –ø–æ–∑–∏—Ü–∏—é –∑–∞–≥–ª—É—à–∫–∏ –≤ —Ç–µ–∫—Å—Ç–µ
        logo_pos = text_with_emoji.find(logo_placeholder_emoji)
        if logo_pos != -1:
            # –í—ã—á–∏—Å–ª—è–µ–º UTF-16 offset –¥–ª—è –ø–æ–∑–∏—Ü–∏–∏ –ª–æ–≥–æ—Ç–∏–ø–∞
            logo_utf16_offset = _utf16_offset(text_with_emoji, logo_pos)
            logo_utf16_length = _utf16_len(logo_placeholder_emoji)
            
            # –°–æ–∑–¥–∞–µ–º entity –¥–ª—è –ª–æ–≥–æ—Ç–∏–ø–∞ (–∑–∞–º–µ–Ω—è–µ—Ç –∑–∞–≥–ª—É—à–∫—É)
            logo_entity = MessageEntity(
                type=MessageEntityType.CUSTOM_EMOJI,
                offset=logo_utf16_offset,
                length=logo_utf16_length,  # –î–ª–∏–Ω–∞ –∑–∞–≥–ª—É—à–∫–∏ –≤ UTF-16
                custom_emoji_id="5188543703018408791"
            )
            
            # –ü—Ä–æ–≤–µ—Ä—è–µ–º, —á—Ç–æ entity –Ω–µ –ø–µ—Ä–µ–∫—Ä—ã–≤–∞–µ—Ç—Å—è —Å –¥—Ä—É–≥–∏–º–∏ entities
            logo_end = logo_utf16_offset + logo_utf16_length
            valid_entities = []
            for entity in entities:
                entity_end = entity.offset + entity.length
                # –ü—Ä–æ–≤–µ—Ä—è–µ–º –ø–µ—Ä–µ–∫—Ä—ã—Ç–∏–µ
                if not (logo_utf16_offset < entity_end and logo_end > entity.offset):
                    valid_entities.append(entity)
                # –ï—Å–ª–∏ –µ—Å—Ç—å –ø–µ—Ä–µ–∫—Ä—ã—Ç–∏–µ, –ø—Ä–æ–ø—É—Å–∫–∞–µ–º —ç—Ç–æ entity (–ª–æ–≥–æ—Ç–∏–ø –∏–º–µ–µ—Ç –ø—Ä–∏–æ—Ä–∏—Ç–µ—Ç)
            
            # –î–æ–±–∞–≤–ª—è–µ–º entity –¥–ª—è –ª–æ–≥–æ—Ç–∏–ø–∞
            valid_entities.append(logo_entity)
            entities = valid_entities
            text = text_with_emoji  # –¢–µ–∫—Å—Ç —É–∂–µ –ø—Ä–∞–≤–∏–ª—å–Ω—ã–π —Å –∑–∞–≥–ª—É—à–∫–æ–π, entity –∑–∞–º–µ–Ω–∏—Ç –µ—ë –Ω–∞ –ª–æ–≥–æ—Ç–∏–ø
        else:
            # –ï—Å–ª–∏ –∑–∞–≥–ª—É—à–∫–∞ –Ω–µ –Ω–∞–π–¥–µ–Ω–∞, –ø—Ä–æ—Å—Ç–æ –ø—Ä–∏–º–µ–Ω—è–µ–º –ø—Ä–µ–º–∏—É–º —ç–º–æ–¥–∑–∏
            text = text_with_emoji
    else:
        text, entities = add_premium_emoji_to_text(text, Config.PREMIUM_EMOJI_MAP)
    
    return text, entities


